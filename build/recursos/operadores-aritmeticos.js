"use strict";
// + => adição
// - => subtração
// * => multiplicação
// / => divisão
// % => resto da divisão
const x = 5;
const y = 2;
let resultado; // undefined até agora
// adição
resultado = x + y;
console.log(resultado); // 7
// subtração
resultado = x - y;
console.log(resultado); // 3
// multiplicação
resultado = x * y;
console.log(resultado); // 10
// divisão
resultado = x / y;
console.log(resultado); // 2.5
// resto da divisão
resultado = x % y;
console.log(resultado); // 1
