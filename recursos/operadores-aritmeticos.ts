// + => adição
// - => subtração
// * => multiplicação
// / => divisão
// % => resto da divisão

const x : number = 5
const y : number = 2
let resultado : number // undefined até agora

// adição
resultado = x + y
console.log(resultado) // 7

// subtração
resultado = x - y
console.log(resultado) // 3

// multiplicação
resultado = x * y
console.log(resultado) // 10

// divisão
resultado = x / y 
console.log(resultado) // 2.5

// resto da divisão
resultado = x % y
console.log(resultado) // 1