// > => maior
// >= => maior ou igual
// < => menor
// <= => menor ou igual
// === igual
// !== diferente
// usado para testar em condições

// maior, maior ou igual
const idade = 20
const maiorDeIdade : boolean = (minhaIdade >= 18) // true

// menor, menor ou igual
const menorDeIdade : boolean = (minhaIdade < 18) // false

// diferente, igual
const minhaAltura: number = 1.80
const suaAltura: number = 1.90

const somosIguais : boolean = (minhaAltura === suaAltura) // false