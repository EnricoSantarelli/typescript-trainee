// concatenação => somar as strings para formar frases
const primeiroNome = "Enrico"
const segundoNome = "Santarelli"
const nomeInteiro = primeiroNome + " " + segundoNome
console.log(nomeInteiro) // Enrico Santarelli

// template string => adicionar variaveis no meio de strings (utilizar ``, não utilizar "" nem '')
const faculdade = "Mauá"
const frase = `Eu estudo na ${faculdade}` // Eu estudo na Mauá
console.log(frase)